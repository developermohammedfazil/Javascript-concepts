// String
//used to store text.written inside quotes
let name = "fazil";
let greeting ='hello,world!';
let message = `welcome,${name}!`;
console.log(name, greeting, message);


//Number
//Represents both integers and floating-point numbers.

let age = 25;
let price = 99.99;
let pi = 3.14;
console.log(age, price , pi);

//Boolean
// respresent true and false values.

let isStudent = true;
let hasJob = false;
console.log(isStudent,hasJob);

//Null
//Represents an intentional empty value.

let value = null;
console.log(value);

//Undefined
//A variable that has been declared but not assigned a value.

let test;
console.log(test);


//Object
//A collection of key-value pairs.

let person = {
    name: "Fazil",
    age: 25,
    isDeveloper: true
};
console.log(person);

//Array
//A special type of object that holds multiple values in an ordered list.
let colors = ["red","blue","green"];
let numbers =[1,2,3,4];
console.log(colors , numbers);